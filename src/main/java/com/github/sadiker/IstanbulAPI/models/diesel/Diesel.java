package com.github.sadiker.IstanbulAPI.models.diesel;

public class Diesel {
    private String katkili;
    private String motorin;
    private String marka;
    
    public String getKatkili() {
        return katkili;
    }
    public void setKatkili(String katkili) {
        this.katkili = katkili;
    }
    public String getMotorin() {
        return motorin;
    }
    public void setMotorin(String benzin) {
        this.motorin = benzin;
    }
    public String getMarka() {
        return marka;
    }
    public void setMarka(String marka) {
        this.marka = marka;
    }

    
}
