package com.github.sadiker.IstanbulAPI.models.gasoline;

public class Gasoline {
    
    private String katkili;
    private String benzin;
    private String marka;
    
    public String getKatkili() {
        return katkili;
    }
    public void setKatkili(String katkili) {
        this.katkili = katkili;
    }
    public String getBenzin() {
        return benzin;
    }
    public void setBenzin(String benzin) {
        this.benzin = benzin;
    }
    public String getMarka() {
        return marka;
    }
    public void setMarka(String marka) {
        this.marka = marka;
    }

    


}
